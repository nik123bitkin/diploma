namespace CompomentLib.TimeEditor
{
    /// <summary>
    /// An action performed on a TimeEditor.
    /// </summary>
    public enum TimeEditorAction
    {
        /// <summary>
        /// No action performed
        /// </summary>
        None,

        /// <summary>
        /// Up button pressed.
        /// </summary>
        UpPressed,

        /// <summary>
        /// Down button pressed
        /// </summary>
        DownPressed,

        /// <summary>
        /// A field value was entered.
        /// </summary>
        ValueEntered
    }
}
